# Azure Data Factory Security Checks
# ============================================================

try {

$dataFactories = Get-AzResource -ResourceType "Microsoft.DataFactory/factories" -ExpandProperties

if (-not $dataFactories -or $dataFactories.Count -eq 0) {

    $results += [PSCustomObject]@{
        Category="DataFactory"
        Check="Data Factory instances"
        Resource="Subscription"
        Status="INFO"
        Details="No Data Factory instances found"
    }

} else {

foreach ($df in $dataFactories) {

$name = $df.Name
$resourceId = $df.ResourceId
$rg = ($resourceId -split "/")[4]

# a. Customer Managed Key
$cmk = $df.Properties.encryption.keyVaultProperties.keyUri
$results += [PSCustomObject]@{
Category="DataFactory"
Check="Customer managed key (CMK)"
Resource=$name
Status=if($cmk){"PASS"}else{"FAIL"}
Details=if($cmk){"CMK configured"}else{"Using Microsoft-managed keys"}
}

# b. Managed Identity
$identityType = $df.Identity.Type
$principalId  = $df.Identity.PrincipalId

$results += [PSCustomObject]@{
Category="DataFactory"
Check="Managed identity enabled"
Resource=$name
Status=if($identityType -and ($identityType -match "SystemAssigned|UserAssigned")){"PASS"}else{"FAIL"}
Details=if($identityType){"Identity: $identityType"}else{"No identity configured"}
}

# c. Azure Key Vault usage (Linked Services)
try {

$linkedServices = Get-AzDataFactoryV2LinkedService `
    -ResourceGroupName $rg `
    -DataFactoryName $name `
    -ErrorAction SilentlyContinue

$usesKeyVault = $false

if ($linkedServices) {
    foreach ($ls in $linkedServices) {

        if ($ls.Properties.Type -eq "AzureKeyVault") {
            $usesKeyVault = $true
            break
        }

        if ($ls.Properties.TypeProperties -match "keyVault") {
            $usesKeyVault = $true
            break
        }
    }
}

$results += [PSCustomObject]@{
Category="DataFactory"
Check="Uses Azure Key Vault for secrets"
Resource=$name
Status=if($usesKeyVault){"PASS"}else{"WARN"}
Details=if($usesKeyVault){"Key Vault used in linked services"}else{"No Key Vault usage detected"}
}

}catch{
$results += [PSCustomObject]@{
Category="DataFactory"
Check="Uses Azure Key Vault for secrets"
Resource=$name
Status="WARN"
Details="Unable to verify linked services"
}
}

# d. RBAC Role Assignments
try {

$roles = Get-AzRoleAssignment -Scope $resourceId -ErrorAction SilentlyContinue

$results += [PSCustomObject]@{
Category="DataFactory"
Check="RBAC roles assigned"
Resource=$name
Status=if($roles -and $roles.Count -gt 0){"PASS"}else{"WARN"}
Details=if($roles){"RBAC roles configured"}else{"No RBAC roles found"}
}

}catch{
$results += [PSCustomObject]@{
Category="DataFactory"
Check="RBAC roles assigned"
Resource=$name
Status="WARN"
Details="Unable to verify RBAC roles"
}
}

}

}

}catch{

$results += [PSCustomObject]@{
Category="DataFactory"
Check="Data Factory scan"
Resource="N/A"
Status="ERROR"
Details=$_.Exception.Message
}

}

# ============================================================
# Azure MySQL Flexible Server Security Checks
# ============================================================

try {

$servers = Get-AzResource -ResourceType "Microsoft.DBforMySQL/flexibleServers"

if (-not $servers -or $servers.Count -eq 0) {

    $results += [PSCustomObject]@{
        Category="MySQL"
        Check="MySQL servers"
        Resource="Subscription"
        Status="INFO"
        Details="No Azure Database for MySQL servers found"
    }

} else {

foreach ($server in $servers) {

$name = $server.Name
$rg   = $server.ResourceGroupName
$resourceId = $server.ResourceId

# Get full details
$details = Get-AzResource -ResourceId $resourceId -ExpandProperties

# a. CMK Encryption
$encType = $details.Properties.dataEncryption.type
$results += [PSCustomObject]@{
Category="MySQL"
Check="Customer managed key (CMK)"
Resource=$name
Status=if($encType -eq "AzureKeyVault"){"PASS"}else{"FAIL"}
Details="Encryption type: $encType"
}

# b. Entra-only Authentication
$auth = $details.Properties.authConfig
$results += [PSCustomObject]@{
Category="MySQL"
Check="Entra-only authentication"
Resource=$name
Status=if($auth.activeDirectoryAuth -eq "Enabled" -and $auth.passwordAuth -eq "Disabled"){"PASS"}else{"FAIL"}
Details="AD Auth: $($auth.activeDirectoryAuth), Password Auth: $($auth.passwordAuth)"
}

# c. Public Network Access
$publicAccess = $details.Properties.network.publicNetworkAccess
$results += [PSCustomObject]@{
Category="MySQL"
Check="Public network access disabled"
Resource=$name
Status=if($publicAccess -eq "Disabled"){"PASS"}else{"FAIL"}
Details="Public Access: $publicAccess"
}

# d. Private Endpoint
$peConnections = Get-AzPrivateEndpointConnection -PrivateLinkResourceId $resourceId -ErrorAction SilentlyContinue
$results += [PSCustomObject]@{
Category="MySQL"
Check="Private endpoint configured"
Resource=$name
Status=if($peConnections){"PASS"}else{"FAIL"}
Details=if($peConnections){"Private endpoint exists"}else{"No private endpoint"}
}

