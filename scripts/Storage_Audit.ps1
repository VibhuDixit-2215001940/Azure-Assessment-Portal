# ── Assessment ──────────────────────────────────────────────
Write-Host "Checking for required Azure modules..." -ForegroundColor Cyan
if (-not (Get-Module -ListAvailable -Name Az.Storage)) {
    Write-Host "Installing Az.Storage module..." -ForegroundColor Yellow
    Install-Module -Name Az.Storage -AllowClobber -Force -Scope CurrentUser
    Import-Module -Name Az.Storage -Force
} else {
    # Check if Get-AzStorageAccountSasPolicy exists, if not update
    if (-not (Get-Command Get-AzStorageAccountSasPolicy -ErrorAction SilentlyContinue)) {
        Write-Host "Updating Az.Storage module to support SAS Policy cmdlets..." -ForegroundColor Yellow
        Update-Module -Name Az.Storage -Force -ErrorAction SilentlyContinue
        Import-Module -Name Az.Storage -Force
    }
}

Write-Host "Connecting to Azure..." -ForegroundColor Cyan
Connect-AzAccount -ErrorAction SilentlyContinue | Out-Null

$results = @()
$context = Get-AzContext
if (-not $context) { 
    Write-Host " ❌ No context found. Please ensure you are logged in." -ForegroundColor Red
    return 
}
$subName = $context.Subscription.Name
$subId   = $context.Subscription.Id
$date    = (Get-Date).ToUniversalTime().AddHours(5.5).ToString("yyyy-MM-dd HH:mm")

Write-Host "Running Storage checks on: $subName ($subId)" -ForegroundColor Yellow

# Add Context Row
$results += [PSCustomObject]@{ Category="Subscription"; Check="Active Context"; Resource=$subName; Status="PASS"; Details="Successfully identified subscription: $subId" }

try {

$accounts = Get-AzStorageAccount

foreach ($sa in $accounts){

$name = $sa.StorageAccountName
$rg = $sa.ResourceGroupName
$ctx = $sa.Context

# Secure Transfer Required
$results += [PSCustomObject]@{
Category="Storage"
Check="Secure transfer required"
Resource=$name
Status=if($sa.EnableHttpsTrafficOnly){"PASS"}else{"FAIL"}
Details=if($sa.EnableHttpsTrafficOnly){"HTTPS enforced"}else{"HTTP allowed"}
}

# Public Blob Access
$results += [PSCustomObject]@{
Category="Storage"
Check="Public blob access disabled"
Resource=$name
Status=if($sa.AllowBlobPublicAccess){"FAIL"}else{"PASS"}
Details=if($sa.AllowBlobPublicAccess){"Public blob access enabled"}else{"Public access disabled"}
}

# Minimum TLS
$results += [PSCustomObject]@{
Category="Storage"
Check="Minimum TLS version"
Resource=$name
Status=if($sa.MinimumTlsVersion -ge "TLS1_2"){"PASS"}else{"FAIL"}
Details="Configured TLS: $($sa.MinimumTlsVersion)"
}

# Network Rules
$results += [PSCustomObject]@{
Category="Storage"
Check="Storage firewall enabled"
Resource=$name
Status=if($sa.NetworkRuleSet.DefaultAction -eq "Deny"){"PASS"}else{"WARN"}
Details=if($sa.NetworkRuleSet.DefaultAction -eq "Deny"){"Restricted network access"}else{"Public access allowed"}
}

# Blob Encryption
$results += [PSCustomObject]@{
Category="Storage"
Check="Blob service encryption"
Resource=$name
Status=if($sa.Encryption.Services.Blob.Enabled){"PASS"}else{"FAIL"}
Details=if($sa.Encryption.Services.Blob.Enabled){"Blob encryption enabled"}else{"Encryption disabled"}
}

# File Encryption
$results += [PSCustomObject]@{
Category="Storage"
Check="File service encryption"
Resource=$name
Status=if($sa.Encryption.Services.File.Enabled){"PASS"}else{"FAIL"}
Details=if($sa.Encryption.Services.File.Enabled){"File encryption enabled"}else{"Encryption disabled"}
}

# Access Key Rotation
try{
$keys = Get-AzStorageAccountKey -ResourceGroupName $rg -Name $name

$rotationIssue = $false
$details = ""

foreach($key in $keys){
    if($key.CreationTime){
        $age = (Get-Date) - $key.CreationTime
        if($age.Days -gt 90){
            $rotationIssue = $true
        }
        $details += "$($key.KeyName) age: $($age.Days) days; "
    }
}

$status = if($rotationIssue){"FAIL"}else{"PASS"}

$results += [PSCustomObject]@{
Category="Storage"
Check="Storage access key rotation"
Resource=$name
Status=$status
Details=$details
}
}catch{
$results += [PSCustomObject]@{
Category="Storage"
Check="Storage access key rotation"
Resource=$name
Status="WARN"
Details="Unable to verify key rotation"
}
}

# Fetch SAS Policy (Safe execution)
$policy = $null
if (Get-Command Get-AzStorageAccountSasPolicy -ErrorAction SilentlyContinue) {
    try {
        $policy = Get-AzStorageAccountSasPolicy -ResourceGroupName $rg -StorageAccountName $name -ErrorAction Stop
    } catch {
        $policy = $null
    }
}

# SAS Expiry
if($policy -and $policy.SasExpirationPeriod){
    if([timespan]$policy.SasExpirationPeriod -le [timespan]"01:00:00"){
        $status="PASS"
    }
    else{
        $status="FAIL"
    }
    $detail="SAS expiration policy: $($policy.SasExpirationPeriod)"
}
else{
    $status="WARN"
    $detail="No SAS expiration policy configured"
}

$results += [PSCustomObject]@{
Category="Storage"
Check="SAS token expiry"
Resource=$name
Status=$status
Details=$detail
}

# SAS HTTPS
if($policy){
    $httpsOnly = $policy.Protocol -eq "Https"
    $results += [PSCustomObject]@{
    Category="Storage"
    Check="SAS HTTPS enforcement"
    Resource=$name
    Status=if($httpsOnly){"PASS"}else{"FAIL"}
    Details="SAS tokens must allow HTTPS only"
    }
}else{
    $results += [PSCustomObject]@{
    Category="Storage"
    Check="SAS HTTPS enforcement"
    Resource=$name
    Status="WARN"
    Details="Could not read SAS policy or no policy defined."
    }
}

# Blob Containers Public Access
try{

$containers = Get-AzStorageContainer -Context $ctx

foreach($c in $containers){

$results += [PSCustomObject]@{
Category="Storage"
Check="Blob container public access"
Resource="$name / $($c.Name)"
Status=if($c.PublicAccess -eq "Off"){"PASS"}else{"FAIL"}
Details=if($c.PublicAccess -eq "Off"){"Private container"}else{"Public access enabled"}
}

}

}catch{}

# Soft Delete
try{

$blobProp = Get-AzStorageServiceProperty -ServiceType Blob -Context $ctx

$results += [PSCustomObject]@{
Category="Storage"
Check="Blob soft delete"
Resource=$name
Status=if($blobProp.DeleteRetentionPolicy.Enabled){"PASS"}else{"WARN"}
Details=if($blobProp.DeleteRetentionPolicy.Enabled){"Enabled ($($blobProp.DeleteRetentionPolicy.Days) days)"}else{"Soft delete disabled"}
}

}catch{}

}

if(-not $accounts){

$results += [PSCustomObject]@{
Category="Storage"
Check="Storage accounts"
Resource="Subscription"
Status="INFO"
Details="No storage accounts found"
}

}

}catch{

$results += [PSCustomObject]@{
Category="Storage"
Check="Storage scan"
Resource="N/A"
Status="ERROR"
Details=$_.Exception.Message
}

}
# ============================================================
# Azure Redis Cache Security Checks
# ============================================================

try {

$redisCaches = Get-AzRedisCache

if (-not $redisCaches -or $redisCaches.Count -eq 0) {

    $results += [PSCustomObject]@{
        Category="Redis"
        Check="Redis instances"
        Resource="Subscription"
        Status="INFO"
        Details="No Redis instances found"
    }

} else {

foreach ($cache in $redisCaches) {

$name = $cache.Name
$rg   = $cache.ResourceGroupName

# Get full resource (latest API for properties)
$resource = Get-AzResource -ResourceId $cache.Id -ApiVersion "2023-08-01" -ExpandProperties

# a. Entra Authentication
$entra = $resource.Properties.disableAccessKeyAuthentication
$results += [PSCustomObject]@{
Category="Redis"
Check="Microsoft Entra Authentication"
Resource=$name
Status=if($entra -eq $true){"PASS"}else{"FAIL"}
Details=if($entra){"Access key auth disabled (Entra enabled)"}else{"Access key auth enabled"}
}

# b. SSL Only
$nonSsl = $resource.Properties.enableNonSslPort
$results += [PSCustomObject]@{
Category="Redis"
Check="SSL only access"
Resource=$name
Status=if($nonSsl -eq $false){"PASS"}else{"FAIL"}
Details=if($nonSsl -eq $false){"Only SSL allowed"}else{"Non-SSL port enabled"}
}

# c. Minimum TLS
$tls = $resource.Properties.minimumTlsVersion
$results += [PSCustomObject]@{
Category="Redis"
Check="Minimum TLS version"
Resource=$name
Status=if($tls -in @("1.2","1.3")){"PASS"}else{"FAIL"}
Details="Configured TLS: $tls"
}

# d. Access Policies (Manual → WARN)
$policies = $resource.Properties.redisConfiguration."accessPolicies"
$results += [PSCustomObject]@{
Category="Redis"
Check="Access policies configured"
Resource=$name
Status=if($policies -and $policies.Count -gt 0){"PASS"}else{"WARN"}
Details=if($policies){"Policies configured"}else{"No access policies found (manual review)"}
}

# e. Managed Identity
$identityType = $resource.Identity.Type
$principalId  = $resource.Identity.PrincipalId
$results += [PSCustomObject]@{
Category="Redis"
Check="System assigned managed identity"
Resource=$name
Status=if($identityType -match "SystemAssigned" -and $principalId){"PASS"}else{"FAIL"}
Details=if($identityType){"Identity: $identityType"}else{"No identity assigned"}
}

# f. Public Network Access
$publicAccess = $resource.Properties.publicNetworkAccess
$isVnet = $resource.Properties.subnetId

$status = if($isVnet){"WARN"} elseif($publicAccess -eq "Disabled"){"PASS"} else {"FAIL"}
$detail = if($isVnet){"VNet deployment"} elseif($publicAccess -eq "Disabled"){"Public access disabled"} else {"Public access enabled"}

$results += [PSCustomObject]@{
Category="Redis"
Check="Public network access"
Resource=$name
Status=$status
Details=$detail
}

# g. Private Endpoint
$privateEndpoints = Get-AzPrivateEndpoint -ResourceGroupName $rg -ErrorAction SilentlyContinue

$matchedPE = $privateEndpoints | Where-Object {
    $_.PrivateLinkServiceConnections.PrivateLinkServiceId -eq $cache.Id
}

$results += [PSCustomObject]@{
Category="Redis"
Check="Private endpoint configured"
Resource=$name
Status=if($matchedPE){"PASS"}else{"FAIL"}
Details=if($matchedPE){"Private endpoint in use"}else{"No private endpoint"}
}

# h. Customer Managed Key (Enterprise)
try {
$enterprise = Get-AzRedisEnterpriseCache -Name $name -ResourceGroupName $rg -ErrorAction SilentlyContinue

if($enterprise){
    $cmk = $enterprise.EnableCustomerManagedKey
    $results += [PSCustomObject]@{
    Category="Redis"
    Check="Customer managed key (CMK)"
    Resource=$name
    Status=if($cmk){"PASS"}else{"FAIL"}
    Details=if($cmk){"CMK enabled"}else{"Using Microsoft-managed keys"}
    }
}
}catch{}

# i. Access Keys Disabled
$accessKeyDisabled = $cache.DisableAccessKeyAuthentication
$results += [PSCustomObject]@{
Category="Redis"
Check="Access key authentication disabled"
Resource=$name
Status=if($accessKeyDisabled){"PASS"}else{"FAIL"}
Details=if($accessKeyDisabled){"Access keys disabled"}else{"Access keys enabled"}
}

# j. Update Channel
$updateChannel = $cache.UpdateChannel
$results += [PSCustomObject]@{
Category="Redis"
Check="Update channel stable"
Resource=$name
Status=if($updateChannel -eq "Stable"){"PASS"}else{"WARN"}
Details="Channel: $updateChannel"
}

}

}

}catch{

$results += [PSCustomObject]@{
Category="Redis"
Check="Redis scan"
Resource="N/A"
Status="ERROR"
Details=$_.Exception.Message
}

}
# ============================================================
# Azure Cosmos DB Security Checks
# ============================================================

try {

$cosmosAccounts = Get-AzResource -ExpandProperties | Where-Object {
    $_.ResourceType -eq "Microsoft.DocumentDB/databaseAccounts" -or
    $_.ResourceType -eq "Microsoft.DocumentDB/mongoClusters"
}

if (-not $cosmosAccounts -or $cosmosAccounts.Count -eq 0) {

    $results += [PSCustomObject]@{
        Category="CosmosDB"
        Check="Cosmos DB accounts"
        Resource="Subscription"
        Status="INFO"
        Details="No Cosmos DB accounts found"
    }

} else {

foreach ($account in $cosmosAccounts) {

$name = $account.Name
$type = $account.ResourceType

# a. Firewall limited to selected networks
$vnetFilter = $account.Properties.isVirtualNetworkFilterEnabled
$results += [PSCustomObject]@{
Category="CosmosDB"
Check="Firewall restricted to selected networks"
Resource=$name
Status=if($vnetFilter -eq $true){"PASS"}else{"FAIL"}
Details=if($vnetFilter){"VNet filtering enabled"}else{"Open to all networks"}
}

# b. Private Endpoint
$privateEndpoints = Get-AzPrivateEndpoint -ErrorAction SilentlyContinue
$matchedPE = $privateEndpoints | Where-Object {
    $_.PrivateLinkServiceConnections.PrivateLinkServiceId -eq $account.ResourceId
}

$status = if($type -eq "Microsoft.DocumentDB/mongoClusters"){"WARN"} elseif($matchedPE){"PASS"} else {"FAIL"}
$detail = if($type -eq "Microsoft.DocumentDB/mongoClusters"){"Not Applicable (Mongo vCore)"} elseif($matchedPE){"Private endpoint configured"} else {"No private endpoint"}

$results += [PSCustomObject]@{
Category="CosmosDB"
Check="Private endpoint configured"
Resource=$name
Status=$status
Details=$detail
}

# c. Disable Local Auth
if ($type -eq "Microsoft.DocumentDB/mongoClusters") {
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Local authentication disabled"
    Resource=$name
    Status="WARN"
    Details="Not Applicable (Mongo vCore)"
    }
} else {
    $disableLocalAuth = $account.Properties.disableLocalAuth
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Local authentication disabled"
    Resource=$name
    Status=if($disableLocalAuth){"PASS"}else{"FAIL"}
    Details=if($disableLocalAuth){"Local auth disabled"}else{"Local auth enabled"}
    }
}

# d. Public Network Access
if ($type -eq "Microsoft.DocumentDB/mongoClusters") {
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Public network access disabled"
    Resource=$name
    Status="WARN"
    Details="Not Applicable (Mongo vCore)"
    }
} else {
    $publicAccess = $account.Properties.publicNetworkAccess
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Public network access disabled"
    Resource=$name
    Status=if($publicAccess -eq "Disabled"){"PASS"}else{"FAIL"}
    Details="Public Access: $publicAccess"
    }
}

# e. Customer Managed Key
if ($type -eq "Microsoft.DocumentDB/mongoClusters") {
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Customer managed key (CMK)"
    Resource=$name
    Status="WARN"
    Details="Not Applicable (Mongo vCore)"
    }
} else {
    $cmk = $account.Properties.keyVaultKeyUri
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Customer managed key (CMK)"
    Resource=$name
    Status=if($cmk){"PASS"}else{"FAIL"}
    Details=if($cmk){"CMK configured"}else{"Using Microsoft-managed keys"}
    }
}

# f. Firewall not allowing all traffic
if ($type -eq "Microsoft.DocumentDB/mongoClusters") {
    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Firewall does not allow all traffic"
    Resource=$name
    Status="WARN"
    Details="Not Applicable (Mongo vCore)"
    }
} else {
    $ipRules = $account.Properties.ipRules
    $allowAll = $false

    if ($ipRules) {
        foreach ($ip in $ipRules) {
            if ($ip.ipAddressOrRange -eq "0.0.0.0") {
                $allowAll = $true
                break
            }
        }
    }

    $results += [PSCustomObject]@{
    Category="CosmosDB"
    Check="Firewall does not allow all traffic"
    Resource=$name
    Status=if($allowAll){"FAIL"}else{"PASS"}
    Details=if($allowAll){"Allows all IPs"}else{"Restricted IP rules"}
    }
}

}

}

}catch{

$results += [PSCustomObject]@{
Category="CosmosDB"
Check="Cosmos DB scan"
Resource="N/A"
Status="ERROR"
Details=$_.Exception.Message
}

}

