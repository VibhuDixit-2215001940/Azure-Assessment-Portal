$pe = Get-AzPrivateEndpointConnection -PrivateLinkResourceId $resourceId -ErrorAction SilentlyContinue
$results += [PSCustomObject]@{
Category="ConfidentialLedger"
Check="Private endpoint configured"
Resource=$name
Status=if($pe){"PASS"}else{"FAIL"}
Details=if($pe){"Private endpoint exists"}else{"No private endpoint"}
}

# Managed Identity
$identity = $details.Identity.Type
$results += [PSCustomObject]@{
Category="ConfidentialLedger"
Check="Managed identity enabled"
Resource=$name
Status=if($identity){"PASS"}else{"FAIL"}
Details=if($identity){"Identity: $identity"}else{"No identity"}
}

# Diagnostic Logs
$diag = Get-AzDiagnosticSetting -ResourceId $resourceId -ErrorAction SilentlyContinue
$results += [PSCustomObject]@{
Category="ConfidentialLedger"
Check="Diagnostic logs enabled"
Resource=$name
Status=if($diag){"PASS"}else{"WARN"}
Details=if($diag){"Configured"}else{"Not configured"}
}

# RBAC
$roles = Get-AzRoleAssignment -Scope $resourceId -ErrorAction SilentlyContinue
$results += [PSCustomObject]@{
Category="ConfidentialLedger"
Check="RBAC configured"
Resource=$name
Status=if($roles){"PASS"}else{"WARN"}
